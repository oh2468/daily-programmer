from itertools import combinations


def has_zero_sum(nums):
    for i, n in enumerate(nums):
        if n == 0:
            return True
        for j in range(i + 1, len(nums)):
            if n + nums[j] == 0:
                return True
    return False

# code golf version
hzs = lambda nums: 0 in nums or len(nums) != len(set(map(abs, nums)))

# bonus challenge
def has_zero_subsets(nums):
    if 0 in nums:
        return True
    for i in range(2, len(nums) + 1):
        for c in combinations(nums, i):
            if sum(c) == 0:
                print(c)
                return True
    return False

# code golf version
hzz = lambda nums: 0 in nums or any(sum(c) == 0 for i in range(2, len(nums) + 1) for c in combinations(nums, i))

